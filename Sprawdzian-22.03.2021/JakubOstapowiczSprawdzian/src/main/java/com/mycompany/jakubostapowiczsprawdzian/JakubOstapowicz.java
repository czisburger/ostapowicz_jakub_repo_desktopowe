    public void addKeyListenerTojo_jTextFieldModel(){
        jo_jTextFieldModel.addKeyListener(new KeyListener() {
            @Override
            public void keyTyped(KeyEvent e) {
                char ch = e.getKeyChar();
                if(ch == 'o' || ch == 'p' || ch == 'r' || ch == 's' || ch == 't' || ch == 'u' || ch == KeyEvent.VK_BACK_SPACE){
                    jo_jTextFieldModel.setEditable(true);
                }
                jo_jTextFieldModel.setEditable(false);
                
            }

            @Override
            public void keyPressed(KeyEvent e) {
            }

            @Override
            public void keyReleased(KeyEvent e) {
            }
        });
        
    }
    
    public void addKeyListenerTojo_jTextFieldPartia(){
        jo_jTextFieldPartia.addKeyListener(new KeyListener() {
            @Override
            public void keyTyped(KeyEvent e) {
                char ch = e.getKeyChar();
                if(ch >= '0' && ch <= '9' || ch == KeyEvent.VK_BACK_SPACE){
                    jo_jTextFieldPartia.setEditable(true);
                }
                jo_jTextFieldPartia.setEditable(false);
            }
                

            @Override
            public void keyPressed(KeyEvent e) {
            }

            @Override
            public void keyReleased(KeyEvent e) {
            }
        });
        
    }

