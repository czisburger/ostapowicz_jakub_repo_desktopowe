        SimpleDateFormat sdf = new SimpleDateFormat("dd-MM-y");
        String text = jTFLogin.getText() + ";" + jTFEmail.getText() + ";" + jSFHeight.getValue() + "cm;" + jSFWeight.getValue() + ";" + sdf.format(jDCDate.getDate());
        stf.saveToFile(text);

        jTFLogin.setText("");
        jTFEmail.setText("");
